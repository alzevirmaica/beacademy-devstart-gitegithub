const job = "Advogado"

switch(job) {
    case "programador":
        console.log("Você é um programador!")
        break
    case "Advogado":
        console.log("Você é um advogado!")
        break
    case "Engenheiro":
        console.log("Você é um engenheiro!")
        break
    default:
        console.log("Profissão não encontrada")
        break
}